public class J7StringSwitch {					// CLASS{
	private void stringSwitch(String foo) {		// VOID, METHOD{
		switch (foo) {							// SWITCH{
		case "new":								// CASE
			break;								// BREAK
		case "exec":							// CASE
		case "pend":							// CASE
			break;								// BREAK
		default:								// CASE
			break;								// BREAK
		}										// }SWITCH
	}											// }METHOD
}												// }CLASS